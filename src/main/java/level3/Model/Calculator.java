package level3.Model;

public class Calculator {
    public int toAdd(int i1, int i2) {
        return i1 + i2;
    }

    public int toSubtract(int i1, int i2) {
        return i1 - i2;
    }

    public int toMultiply(int i1, int i2) {
        return i1 * i2;
    }

    public int toDivide(int i1, int i2) {
        return i1 / i2;
    }
}
