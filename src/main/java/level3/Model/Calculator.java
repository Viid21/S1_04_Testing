package level3.Model;

public class Calculator {
    public int add(int i1, int i2) {
        return i1 + i2;
    }

    public int subtract(int i1, int i2) {
        return i1 - i2;
    }

    public int multiply(int i1, int i2) {
        return i1 * i2;
    }

    public int divide(int i1, int i2) {
        return i1 / i2;
    }
}
